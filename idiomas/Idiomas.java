package com.aduran.educacionit.practicasemana11.idiomas;

public abstract class Idiomas {
	
	private String ingles;
	private String mandarin;
	private String frances;
	private String aleman;
	private String español;
	private String portugues;
	
	
	public String getIngles() {
		return ingles;
	}
	public void setIngles(String ingles) {
		this.ingles = ingles;
	}
	public String getMandarin() {
		return mandarin;
	}
	public void setMandarin(String mandarin) {
		this.mandarin = mandarin;
	}
	public String getFrances() {
		return frances;
	}
	public void setFrances(String frances) {
		this.frances = frances;
	}
	public String getAleman() {
		return aleman;
	}
	public void setAleman(String aleman) {
		this.aleman = aleman;
	}
	public String getEspañol() {
		return español;
	}
	public void setEspañol(String español) {
		this.español = español;
	}
	public String getPortugues() {
		return portugues;
	}
	public void setPortugues(String portugues) {
		this.portugues = portugues;
	}
	
	
	
	

}
